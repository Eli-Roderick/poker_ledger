create or replace function public.game_participant_totals(
  p_session_ids bigint[]
)
returns table (
  sp_id bigint,
  session_id bigint,
  player_id bigint,
  profile_id uuid,
  display_name_snapshot text,
  buy_in_cents_total bigint,
  cash_out_cents bigint,
  net_cents bigint,
  paid_upfront boolean,
  settlement_done boolean,
  player_name text,
  player_email text,
  player_active boolean,
  linked_user_id uuid,
  was_linked_to_deleted_user boolean,
  ledger_version integer,
  finalized boolean
)
language sql
stable
security definer
set search_path = ''
as $$
  with visible_participants as (
    select
      participant.*,
      game.ledger_version,
      game.finalized
    from public.session_players participant
    join public.sessions game on game.id = participant.session_id
    where participant.session_id = any(p_session_ids)
      and public.can_view_session(participant.session_id)
  ),
  v2_money as (
    select
      event.participant_id,
      coalesce(sum(
        case
          when event.event_type in ('initial_buy_in', 'rebuy')
            then event.amount_cents
          when event.event_type = 'correction'
               and event.amount_cents > 0
            then event.amount_cents
          when event.event_type = 'reversal'
               and original.amount_cents > 0
            then event.amount_cents
          else 0
        end
      ), 0)::bigint as buy_ins,
      coalesce(sum(
        case
          when event.event_type = 'cash_out'
            then -event.amount_cents
          when event.event_type = 'correction'
               and event.amount_cents < 0
            then -event.amount_cents
          when event.event_type = 'reversal'
               and original.amount_cents < 0
            then -event.amount_cents
          else 0
        end
      ), 0)::bigint as cash_outs
    from public.ledger_events event
    left join public.ledger_events original
      on original.id = event.reverses_event_id
    where event.session_id = any(p_session_ids)
    group by event.participant_id
  )
  select
    participant.id,
    participant.session_id,
    coalesce(participant.player_id, -participant.id),
    participant.profile_id,
    participant.display_name_snapshot,
    case
      when participant.ledger_version = 2 then coalesce(money.buy_ins, 0)
      else coalesce(participant.buy_in_cents_total, 0)
    end::bigint,
    case
      when participant.ledger_version = 2
        then coalesce(money.cash_outs, 0)
      else coalesce(participant.cash_out_cents, 0)
    end::bigint,
    case
      when participant.ledger_version = 2
        then coalesce(money.cash_outs, 0) - coalesce(money.buy_ins, 0)
      else coalesce(participant.cash_out_cents, 0)
           - coalesce(participant.buy_in_cents_total, 0)
    end::bigint,
    participant.paid_upfront,
    participant.settlement_done,
    case
      when participant.ledger_version = 2
        then coalesce(participant.display_name_snapshot, 'Deleted player')
      else player.name
    end,
    null::text,
    case
      when participant.ledger_version = 2 then true
      else coalesce(player.active, false)
    end,
    case
      when participant.ledger_version = 2 then participant.profile_id
      else player.linked_user_id
    end,
    case
      when participant.ledger_version = 2 then false
      else coalesce(player.was_linked_to_deleted_user, false)
    end,
    participant.ledger_version,
    participant.finalized
  from visible_participants participant
  left join public.players player on player.id = participant.player_id
  left join v2_money money on money.participant_id = participant.id;
$$;

