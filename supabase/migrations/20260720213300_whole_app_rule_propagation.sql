create or replace function public.my_games()
returns setof public.sessions
language sql
stable
security definer
set search_path = ''
as $$
  select game.*
  from public.sessions game
  where public.can_view_session(game.id)
    and (
      game.user_id = auth.uid()
      or game.current_host_id = auth.uid()
      or exists (
        select 1
        from public.session_players participant
        left join public.players legacy_player
          on legacy_player.id = participant.player_id
        where participant.session_id = game.id
          and participant.removed_at is null
          and (
            participant.profile_id = auth.uid()
            or (
              game.ledger_version = 1
              and legacy_player.linked_user_id = auth.uid()
            )
          )
      )
    )
  order by game.started_at desc;
$$;

