-- Propagate finalized-ledger rules into transfer tracking and close remaining
-- group archival compatibility gaps.

create table if not exists public.settlement_transfer_status_history (
  id bigint generated by default as identity primary key,
  transfer_id bigint not null
    references public.settlement_transfers(id) on delete restrict,
  previous_status text not null,
  new_status text not null,
  changed_by uuid references auth.users(id) on delete set null,
  changed_at timestamptz not null default now(),
  check (previous_status in ('pending', 'paid', 'received', 'disputed')),
  check (new_status in ('pending', 'paid', 'received', 'disputed'))
);
alter table public.settlement_transfer_status_history
  add column if not exists changed_by_snapshot text;
update public.settlement_transfer_status_history history
set changed_by_snapshot = profile.display_name
from public.profiles profile
where history.changed_by = profile.id
  and history.changed_by_snapshot is null;
create or replace function private.snapshot_transfer_status_actor()
returns trigger
language plpgsql
security definer
set search_path = ''
as $$
begin
  if new.changed_by_snapshot is null and new.changed_by is not null then
    select display_name into new.changed_by_snapshot
    from public.profiles
    where id = new.changed_by;
  end if;
  return new;
end;
$$;
drop trigger if exists transfer_status_snapshot_actor
