create or replace function public.set_v2_backup_host(
  p_session_id bigint,
  p_profile_id uuid,
  p_idempotency_key uuid
)
returns jsonb
language plpgsql
security definer
set search_path = ''
as $$
declare
  actor uuid := private.require_actor();
  game public.sessions;
  prior jsonb;
  result jsonb;
begin
  perform private.require_compatible_client();
  prior := private.begin_idempotent(
    actor, p_idempotency_key, 'set_v2_backup_host',
    jsonb_build_object(
      'session_id', p_session_id,
      'profile_id', p_profile_id
    )
  );
  if prior is not null then return prior; end if;
  game := private.require_v2_host(p_session_id);
  if game.phase not in ('draft', 'live', 'settling') then
    raise exception 'Backup host cannot be changed in this phase'
      using errcode = '22023';
  end if;
  if p_profile_id = actor
     or not exists (
       select 1 from public.profiles profile
       where profile.id = p_profile_id
         and profile.deleted_at is null
         and profile.suspended_at is null
     )
     or not (
       exists (
         select 1 from public.session_players participant
         where participant.session_id = p_session_id
           and participant.profile_id = p_profile_id
           and participant.accepted_at is not null
           and participant.removed_at is null
       )
       or (
         game.group_id is not null
         and private.can_manage_group(game.group_id, p_profile_id)
       )
     ) then
    raise exception
      'Choose another accepted participant or authorized group manager'
      using errcode = '22023';
  end if;
  update public.sessions
  set backup_host_id = p_profile_id,
      updated_at = now()
  where id = p_session_id;
  result := jsonb_build_object(
    'session_id', p_session_id,
    'backup_host_id', p_profile_id
  );
  perform private.complete_idempotent(actor, p_idempotency_key, result);
  return result;
end;
$$;

revoke all on function public.set_v2_backup_host(bigint, uuid, uuid)
  from public, anon;
grant execute on function public.set_v2_backup_host(bigint, uuid, uuid)
  to authenticated;

