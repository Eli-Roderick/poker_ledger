create or replace function private.require_v2_host(p_session_id bigint)
returns public.sessions
language plpgsql
security definer
set search_path = ''
as $$
declare
  actor uuid := private.require_actor();
  game public.sessions;
begin
  select * into game
  from public.sessions
  where id = p_session_id
  for update;
  if not found or game.ledger_version <> 2 then
    raise exception 'Game not found' using errcode = 'P0002';
  end if;
  if game.current_host_id is distinct from actor then
    raise exception 'Only the current host can perform this action'
      using errcode = '42501';
  end if;
  if exists (
    select 1
    from public.groups grouped
    where grouped.id = game.group_id
      and grouped.archived_at is not null
  ) then
    raise exception 'Archived group games are read-only'
      using errcode = '55000';
  end if;
  return game;
end;
$$;

